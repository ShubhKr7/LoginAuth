const mongoose=require('mongoose');

const authSchema=new mongoose.Schema({
    id: {
        type: Number,
        required: true,
        unique: true
    },
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true,
    }
});

const authenticationDataset = mongoose.model("authDataset", authSchema);

module.exports=authenticationDataset;